package com.speldipn.example.fc_java.Loop.src.com.speldipn.loop;

public class For {

	public static void main(String[] args) {
		// --------------
		// for문의 기본구조 
		// --------------
		
		for(int i = 0; i < 10; ++i) {
			System.out.println(i);
		}
		
		for(;;) {
			System.out.println("출력");
		}
	}

}
