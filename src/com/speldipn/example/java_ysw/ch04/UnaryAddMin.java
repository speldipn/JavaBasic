public class UnaryAddMin {
  public static void main(String[] args) {
    int n1 = 5;
    System.out.println(+n1);
    System.out.println(-n1);

    short n2 = 7;
    int n3 = +n2;
    int n4 = -n2;
    System.out.println(n3);
    System.out.println(n4);
  }
}
