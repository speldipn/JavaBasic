public class CompOp {
  public static void main(String[] args) {
    int A = 10, B = 20;

    if (true) {
      System.out.println("true");
    } else {
      System.out.println("false");
    }

    if (A > B) {
      System.out.println("A > B true");
    } else {
      System.out.println("A > B false");
    }

    if (A != B) {
      System.out.println("A != B true");
    } else {
      System.out.println("A != B false");
    }
  }
}
